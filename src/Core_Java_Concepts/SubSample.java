public class SubSample {
    String name;
    int age;
    float percentage;

    SubSample(String a, int b, float c){
        this.name=a;
        this.age=b;
        this.percentage=c;
    }
    SubSample(){
        this.name="Gokul Gangadharan";
        this.age=28;
        this.percentage=78.45F;
    }
}
